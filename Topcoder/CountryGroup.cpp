class CountryGroup {
public:
	int solve(vector <int> a){
	int i,j,n,ans=0;
	n=a.size();
	for(i=0;i<n;){
	///	cout << i << endl;
		for(j=i;j<a[i]+i;j++){
			if(j>=n)return -1;
			if(a[i]!=a[j])return -1;
		}
		i=j;
		ans++;
	}
	return ans;
	}
};


