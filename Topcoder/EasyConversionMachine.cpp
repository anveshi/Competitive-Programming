class EasyConversionMachine {
public:
	string isItPossible(string originalWord, string finalWord, int k) {
	int l1,l2;
	l1=originalWord.size();
	l2=finalWord.size();
	if(l1!=l2)return "IMPOSSIBLE";
	else{
		int h=0;
		for(int i=0;i<l1;i++)if(originalWord[i]!=finalWord[i])h++;
		if(k<h)return "IMPOSSIBLE";
		else if((h-k)%2==0) return "POSSIBLE";
		else return "IMPOSSIBLE";
	}
	}
};


